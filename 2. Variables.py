# Variables in Python

# Integer variable
age = 25

# Float variable
height = 5.9

# String variable
name = "John Doe"

# Boolean variable
is_student = True

# List variable
fruits = ["apple", "banana", "cherry"]

# Dictionary variable
person = {
    "name": "Alice",
    "age": 30,
    "city": "New York"
}

# Tuple variable
coordinates = (40.7128, 74.0060)

# Set variable
unique_numbers = {1, 2, 3, 4, 5}

# Print variables
print("Age:", age)
print("Height:", height)
print("Name:", name)
print("Is student:", is_student)
print("Fruits:", fruits)
print("Person:", person)
print("Coordinates:", coordinates)
print("Unique numbers:", unique_numbers)
